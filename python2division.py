# python2.2前的版本和python2.2以后3.0以前的版本的默认情况下，
# "/"所做的除法是以一种两个数或者多个数出现一个浮点数结果就以浮点数的形式表示，即float除法
# "//"所做的除法则不相同，"//"不管两者出现任何数，都以整除结果为准，不对小数部分进行处理，直接抛弃，也就是整除法
