print(3/2)
# 1
print(3/2.0)
# 1.5
print(3.0/2)
# 1.5
print(10/3.0)
# 3.3333333333333335

print(3//2)
# 1
print(3//2.0)
# 1.0
print(3.0//2)
# 1.0
print(3.0//2.0)
# 1.0